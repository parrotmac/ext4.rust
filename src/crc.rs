#[inline]
fn do_crc16(crc: u16, buf: &[u8], tab: [u16; 256]) -> u16 {
    buf.iter().fold(crc, |acc, b| {
        tab[((acc ^ *b as u16) & 0xff) as usize] ^ (acc >> 8)
    })
}

#[inline]
fn do_crc32(crc: u32, buf: &[u8], tab: [u32; 256]) -> u32 {
    buf.iter().fold(crc, |acc, b| {
        tab[((acc ^ *b as u32) & 0xff) as usize] ^ (acc >> 8)
    })
}

